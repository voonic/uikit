/// Different types of button available in this UI kit.
enum ButtonType {
  /// Uses the prmiary color from settings to draw buttons.
  primary,

  /// Uses the warning color from settings to draw buttons.
  warning,

  /// Uses the danger color from settings to draw buttons.
  danger,

  /// Uses the success color from settings to draw buttons.
  success,

  /// Uses the white color from settings to draw buttons.
  white,
}
